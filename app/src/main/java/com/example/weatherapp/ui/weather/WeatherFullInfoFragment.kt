package com.example.weatherapp.ui.weather

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.fragment.app.Fragment
import androidx.lifecycle.ViewModelProvider
import com.example.weatherapp.databinding.FragmentWeatherFullInfoBinding

class WeatherFullInfoFragment : Fragment() {

    private lateinit var weatherFullInfoViewModel: WeatherFullInfoViewModel
    private var _binding: FragmentWeatherFullInfoBinding? = null

    // This property is only valid between onCreateView and
    // onDestroyView.
    private val binding get() = _binding!!

    override fun onCreateView(
            inflater: LayoutInflater,
            container: ViewGroup?,
            savedInstanceState: Bundle?
    ): View? {
        weatherFullInfoViewModel =
                ViewModelProvider(this).get(WeatherFullInfoViewModel::class.java)

        _binding = FragmentWeatherFullInfoBinding.inflate(inflater, container, false)
        val root: View = binding.root

        return root
    }

    override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}